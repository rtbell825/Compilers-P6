	addu  $sp, $sp, 4
	move  $t0, $t1
	sw    $t0, _globalInt		# store global variable globalInt
		#ASSIGN
	lw    $t0, -12($fp)	# Load local variable b
	sw    $t0, 0($sp)	#PUSH
	subu  $sp, $sp, 4
	lw    $t0, 4($sp)	#POP
	addu  $sp, $sp, 4
	li    $t0, 0
	sw    $t0, 0($sp)	#PUSH
	subu  $sp, $sp, 4
	lw    $t1, 4($sp)	#POP
	addu  $sp, $sp, 4
	move  $t0, $t1
	sw    $t0, -12($fp)	# store local variable b
		#READ
	li    $v0, 5
	syscall
	sw    $v0, 0($sp)	#PUSH
	subu  $sp, $sp, 4
	lw    $t0, _globalInt		# Load global variable globalInt
